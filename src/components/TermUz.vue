        </div>
      </div>
      <div class="subject">
        6. Mahsulotni qabul qilib olish va yetkazib berish shartlari
      </div>
      <div class="text">
        <div class="title">
          <span class="tab"></span>6.1. Isteʼmolchi tanlagan mahsulotini
          buyurtmada ko‘rsatgan va o‘zi tanlagan chakana savdo do‘konidan
          mustaqil ravishda olishi mumkin.
        </div>
        <div class="title">
          <span class="tab"></span>6.2. Isteʼmolchi chakana savdo do‘konida
          mahsulotlar haqida kerakli va ishonchli maʼlumotlarni oladi:<br />
          <span class="tabs"></span>- mahsulot nomi;<br />
          <span class="tabs"></span>- mahsulotning shtrix kodi (mavjud
          bo‘lsa);<br />
          <span class="tabs"></span>- mahsulotdan samarali va xavfsiz
          foydalanish qoidalari va shartlari;<br />
          <span class="tabs"></span>- kafolat muddati (agar mahsulot uchun
          belgilangan bo‘lsa);<br />
          <span class="tabs"></span>- agar bunday muddat mahsulot uchun
          belgilangan bo‘lsa, xizmat yoki yaroqlilik muddati, shuningdek,
          belgilangan muddatlar o‘tgandan keyingi xaridorning zaruriy
          harakatlari to‘g‘risidagi maʼlumotlar va agar zaruriy harakatlar
          amalga oshirilmasa, yuzaga kelishi mumkin bo‘lgan oqibatlari, agar
          mahsulotlar ko‘rsatilgan muddat o‘tishi bilan Isteʼmolchining hayoti,
          sog‘lig‘i va mol-mulkiga xavf tug‘dirsa yoki undan maqsadli
          foydalanish uchun yaroqsiz bo‘lib qolishi;<br />
          <span class="tabs"></span>- ushbu oferta shartlariga muvofiq
          mahsulotlarni sotib olish narxi va shartlari;<br />
          <span class="tabs"></span>- mahsulotning muvofiqligi (mos kelishi)
          bo‘yicha bo‘lgan majburiy talablari, texnik reglamentlar yoki
          standartlar belgilari;<br />
          <span class="tabs"></span>- mahsulotlarning asosiy isteʼmol
          xususiyatlari to‘g‘risidagi maʼlumotlar (agar ular qadoqda
          ko‘rsatilmagan bo‘lsa);<br />
          <span class="tabs"></span>- majburiy sertifikatlanishi lozim bo‘lgan
          mahsulotlarga muvofiqlik sertifikati;<br />
          <span class="tabs"></span>- mahsulot bilan yoki uning qo‘llanilishi
          (undan foydalanish) bilan bog‘liq boshqa maʼlumotlar.
        </div>
        <div class="title">
          <span class="tab"></span>6.3. Mahsulotlarni sotib olish vaqtida
          Isteʼmolchiga mustaqil ravishda yoki maslahatchi yordamida, mahsulot
          bilan tanishish, unga berilgan mahsulotlarning miqdori va to‘plami
          to‘liqligini tekshirish imkoniyati beriladi.
        </div>
        <div class="title">
          <span class="tab"></span>6.4. O‘zbekiston Respublikasida chakana savdo
          qoidalarida (Vazirlar Mahkamasining 2003 yil 13 fevraldagi 75-son
          qaroriga 1-ilova) hamda mahsulotning xususiyatiga ko‘ra taʼqiqlanmagan
          bo‘lsa, Isteʼmolchi taklif etilayotgan mahsulotni ko‘zdan kechirishga,
          o‘zining ishtirokida maxsulotning xususiyatlarini tekshirishni,
          ishlashini namoyish qilishni talab qilishga haqli.
        </div>
        <div class="title">
          <span class="tab"></span>6.5. Isteʼmolchi mahsulotni tegishli sifatda,
          idishda va (yoki) qadoqda, maʼlum bir to‘plamda va to‘liq holda,
          mahsulotga tegishli hujjatlar va aksessuarlar bilan birga oladi.
        </div>
        <div class="title">
          <span class="tab"></span>6.6. Yetkazib berish kuryerlik xizmati orqali
          amalga oshiriladi. Yetkazib berish narxi va shartlari kuryerlik
          xizmati tomonidan Isteʼmolchi bilan kelishib olinadi.
        </div>
        <div class="title">
          <span class="tab"></span>6.7. Isteʼmolchi etkazib berilgan
          mahsulotning yaxlitligi, yaroqliligi, buyurtma qilingan miqdorga,
          sifatiga va assortimentiga muvofiqligini kurer ishtirokida tekshirib
          olishi shart. Agar mahsulotlar sifatsiz bo‘lsa yoki buyurtma qilingan
          miqdor va/yoki assortimentga mos kelmasa, buyurtma qilingan mahsulotni
          rad etish mumkin. Yetkazib berilgan mahsulotlar Isteʼmolchiga qabul
          qilish va topshirish dalolatnomasini va/yoki yuk xatini kurer bilan
          imzolash orqali topshiriladi. Tushunmovchiliklarga yo‘l qo‘yilmaslik
          uchun kuryer Isteʼmolchidan shaxsini tasdiqlovchi hujjatni taqdim
          etishni so‘rashga va ushbu hujjatning maʼlumotlarini qabul qilish va
          topshirish dalolatnomasida ko‘rsatishga haqli.
        </div>
      </div>
      <div class="subject">
        7. Mahsulotlarni qaytarish va almashtirish shartlari
      </div>
      <div class="text">
        <div class="title">
          <span class="tab"></span>7.1. Zarur sifatga ega mahsulotlarni
          qaytarish.
        </div>
        <div class="title">
          <span class="tab"></span>7.1.1. Isteʼmolchi buyurtma qilingan sifatli
          mahsulotni istalgan vaqtda, olishdan oldin, kurer tomonidan mahsulot
          yetkazib berish vaqtida, shuningdek olinganidan keyin - 24 soat ichida
          sabab ko‘rsatmasdan rad etishga haqli. Mahsulotni sotilgandagi holati
          (qadog‘i, muhrlar, yorliqlar) va isteʼmol/foydalanish xususiyatlari
          saqlangan holda hamda, uni sotib olish shartlarini tasdiqlovchi
          (hisob-faktura yoki to‘lov hujjati) hujjat mavjud bo‘lgan holda
          mahsulotlarni qaytarish yoki almashtirish mumkin. O‘zbekiston
          Respublikasi Vazirlar Mahkamasining 2003-yil 13-fevraldagi 75-son
          qarori bilan tasdiqlangan “O‘zbekiston Respublikasida chakana savdo
          qoidalari”ning 1-ilovasidagi Ro‘yxatdagi sifatli mahsulotlar
          qaytarilishi mumkin emas.
        </div>
        <div class="title">
          <span class="tab"></span>7.1.2. Mahsulotni qaytarish ----------
          veb-saytda qaytarish buyurtmasini (ushbu ofertaning 2-ilovasi)
          rasmiylashtirish orqali amalga oshiriladi.
        </div>
        <div class="title">
          <span class="tab"></span>7.1.3. Zarur sifatga ega mahsulotni qaytarish
          yoki almashtirish uchun yetkazib berish xarajatlari Isteʼmolchi
          hisobidan amalga oshiriladi.
        </div>
        <div class="title">
          <span class="tab"></span>7.2. Sifatsiz mahsulotlarni qaytarish:
        </div>
        <div class="title">
          <span class="tab"></span>7.2.1. Isteʼmolchi sifatsiz mahsulotni
          olganda o‘z xohishiga ko‘ra quyidagilarni talab qilishga haqli: <br />
          <span class="tabs"></span>- o‘xshash markadagi (model, buyum)
          mahsulotga almashtirishni; <br />
          <span class="tabs"></span>- sotib olish narxini mos ravishda qayta
          hisoblash orqali boshqa (model, buyum) mahsulotga almashtirish; <br />
          <span class="tabs"></span>- sotib olish narxini mutanosib ravishda
          pasaytirish; <br />
          <span class="tabs"></span>- mahsulotdagi nuqsonlarni zudlik bilan
          bepul bartaraf etish; <br />
          <span class="tabs"></span>- mahsulotdagi nuqsonlarni bartaraf etish
          uchun qilingan xarajatlarni qoplash;
          <br />
          <span class="tabs"></span>- mahsulotlarni rad etish va pul
          mablag‘larini qaytarish; <br />
          <span class="tabs"></span>- shartnomani bekor qilish.
        </div>
        <div class="title">
          <span class="tab"></span>7.2.2. Sifatsiz mahsulotlarni qaytarish ushbu
          shartnomaning 7.1.2-bandda ko‘rsatilgan tartibda amalga oshiriladi.
        </div>
        <div class="title">
          <span class="tab"></span>7.2.3. Qaytarilish buyurtmasida Isteʼmolchi
          tomonidan qaytarilgan mahsulotning nuqsoni aniq ko‘rsatilishi kerak.
        </div>
        <div class="title">
          <span class="tab"></span>7.2.4. Mahsulot qaytarish yoki almashtirish
          uchun mustaqil yetkazib berilgan taqdirda, Isteʼmolchi tomonidan
          mahsulot 3 (uch) ish kuni ichida yetkazib berishi shart. Mahsulotlar
          kuryerlik xizmatlari orqali yetkazib berilganda, kuryerlik xizmatining
          ichki hujjatlarida (pochta markasi, shtamp va boshqalar) nazarda
          tutilgan usullar bilan jo‘natishda, kuryerlik xizmati tomonidan
          ko‘rsatilgan sana mahsulot jo‘natilgan sana hisoblanadi.
        </div>
        <div class="title">
          <span class="tab"></span>7.2.5. Qisman oldindan to‘lov amalga
          oshirilgan mahsulotlar qaytarilganda, oldindan to‘lov mahsulot
          Sotuvchining ixtiyoriga o‘tgan kundan boshlab, 10 ish kuni ichida
          qaytariladi. Mablag‘lar buyurtmada ko‘rsatilgan bank plastik kartasiga
          yoki qonun hujjatlarida taʼqiqlanmagan boshqa usullar bilan
          qaytarilishi mumkin.
        </div>
        <div class="title">
          <span class="tab"></span>7.2.6. Kafolat shartlariga amal qilgan holda,
          Isteʼmolchi mahsulotning nosozligini bartaraf etish uchun chakana
          savdo do‘koniga yoki servis markaziga mustaqil ravishda murojaat
          qiladi. Bunday holda, Sotuvchi Isteʼmolchi oldida kafolat shartlari
          bajarilishi uchun javobgardir.
        </div>
      </div>
